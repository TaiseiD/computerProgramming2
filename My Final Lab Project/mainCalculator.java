    private void btnNegativeSignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNegativeSignActionPerformed
        // TODO add your handling code here:
        String text = output.getText();
        
        if (Operator == null || Operator.isEmpty() || !text.contains(" " + Operator + " ")) {
            double val = Double.parseDouble(text);
            val *= -1;
            output.setText(String.valueOf(val));
        } else {
            String[] parts = text.split(" \\" + Operator + " ");
            
            if (parts.length == 2) {
                String left = parts[0];
                String right = parts[1];
                
                if (!right.isEmpty()) {
                
                    try {
                        double val = Double.parseDouble(right);
                        val *= -1;
                        output.setText(left + " " + Operator + " " + val);
                    } catch (NumberFormatException e) {
                    output.setText("Error");
                    }
                }
            }
        }
    }//GEN-LAST:event_btnNegativeSignActionPerformed

