    private void btnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualActionPerformed
        // TODO add your handling code here:
        String[] parts = output.getText().split(" ");

        if (parts.length == 3) {
            try {
                num1 = Double.parseDouble(parts[0]);
                Operator = parts[1];
                num2 = Double.parseDouble(parts[2]);

                switch (Operator) {
                    case "+":
                        result = num1 + num2;
                        break;
                    case "-":
                        result = num1 - num2;
                        break;
                    case "*":
                        result = num1 * num2;
                        break;
                    case "/":
                        result = num2 != 0 ? num1 / num2 : Double.NaN;
                        break;
                    case "%":
                        if (num2 != 0) {
                            result = num1 % num2;
                        } else {
                            output.setText("Cannot divide by zero!");
                            return;
                        }
                        break;
                    case "^":
                        result = Math.pow(num1, num2);
                        break;
                    default:
                        output.setText("Unknown operator");
                        return;
                }

                if (Operator.equals("%")) {
                    output.setText("Remainder: " + String.format("%8f", result).replaceAll("\\.?0+$", ""));
                } else {
                output.setText(String.format("%8f", result).replaceAll("\\.?0+$", ""));
                }

            } catch (NumberFormatException e) {
                output.setText("Invalid number");
            }
        } else {
            output.setText("Invalid input");
        }
    }//GEN-LAST:event_btnEqualActionPerformed

